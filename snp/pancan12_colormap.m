function cmap = pancan12_colormap
% return containers.Map of TCGA cancer type acronym -> RGB color vector
pc_names = {'BLCA', 'BRCA', 'COAD', 'GBM', 'HNSC', 'KIRC', 'LAML', 'LUAD', 'LUSC', 'OV', 'READ', 'UCEC'};

pc_colors = {...
    hex2dec(['A6';'CE';'E3'])/255', ...
    hex2dec(['1F';'78';'B4'])/255', ...
    hex2dec(['B2';'DF';'8A'])/255', ...
    hex2dec(['33';'A0';'2C'])/255', ...
    hex2dec(['FB';'9A';'99'])/255', ...
    hex2dec(['E3';'1A';'1C'])/255', ...
    hex2dec(['FD';'BF';'6F'])/255', ...
    hex2dec(['FF';'7F';'00'])/255', ...
    hex2dec(['CA';'B2';'D6'])/255', ...
    hex2dec(['6A';'3D';'9A'])/255', ...
    hex2dec(['FF';'FF';'99'])/255', ...
    hex2dec(['B1';'59';'28'])/255 ...
};

cmap = containers.Map(pc_names,pc_colors);
cmap('CRC') = cmap('COAD');