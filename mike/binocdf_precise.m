% incomplete beta function = cumulative binomial
p = 10e-6;
N = 50000;
n = 0:200;
subplot(2,1,1);
  p1 = binopdf(n,N,p);
  plot(n,p1,'r.');
subplot(2,1,2);
  c1 = 1-binocdf(n,N,p);
  c2 = betainc(1-p,N-n,n+1,'upper');
  plot(n,-log10(c1),'ro',n,-log10(c2),'b.');
